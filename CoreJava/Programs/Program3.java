/* Write a program to test if a number is positive or negative */

// import java.lang.*;

class Program3Demo{
    public static void main(String args[]){
        int num = -5;
        if(num == 0)
            System.out.println("It is a Zero!");
        else if(num > 0)
            System.out.println(num+"is positive");
        else
            System.out.println(num+"is negative");
    }
}
