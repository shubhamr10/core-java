/*
*  ==>  FOR EACH LOOP
* Write a program to see the use of for-each loop
* and retrevie the element from an array
*  */

import java.lang.*;

class Program9Demo{
    public static void main(String args[]){
        int arr[] = {200, 19, -50, 44, 99};

//        for each statement
        for (int i:arr) {
            System.out.println(i);
        }
    }
}
